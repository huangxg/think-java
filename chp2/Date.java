public class Date {
    public static void main(String[] args) {
        String day = "Friday";
        int date = 13;
        String month = "January";
        int year = 2022;
        // American format
        System.out.println(day + ", " + month + " " + date + ", " + year);
        // European format
        System.out.println(day + " " + date + " " + month + " " + year);
    }
}
